let rad = prompt("Enter Radius of Circle ");
let area = Math.PI * parseInt(rad) * parseInt(rad);
document.write("Area of Circle is " + area + "<br>");

let val = prompt("Enter Value");
let sqrroot = Math.sqrt(parseInt(val));
document.write("The SQR =  " + sqrroot + "<br>");

let angl = prompt("Enter Angle for Cosine");
let res = Math.cos(parseInt(angl));
document.write("The Cosine Value=  " + res + "<br>");
